
public class RandGeneratedValue {
	private int firstDecimal;
	
	private Double randValue;
	
	private int digitCount;

	public int getFirstDecimal() {
		return firstDecimal;
	}

	public void setFirstDecimal(int firstDecimal) {
		this.firstDecimal = firstDecimal;
	}

	public Double getRandValue() {
		return randValue;
	}

	public void setRandValue(Double randValue) {
		this.randValue = randValue;
	}

	public int getDigitCount() {
		return digitCount;
	}

	public void setDigitCount(int digitCount) {
		this.digitCount = digitCount;
	}
	
}
